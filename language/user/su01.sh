#!/usr/bin/bash  

# switch who is the current user command...  

su username

# ...issuing this command, will change whoever is signed in as being the current user of the system;    
# it will, also, ask you to type in what is that username's password to confirm who is signing in.  
# NOTE: In order to get out...just type...
# exit
# ...which will return you back to what was the previously signed in user.

# A next variation of this command is...  
# su -
# ...which will sign in the 'root' user;   
# it will prompt you to enter what is the root users password, of course;  
# before letting you in as root.    


